public class while_loop {
    public static void main(String[] args) {
        System.out.print("Hiển thị các số từ 1 đến 10 (sử dụng vòng lặp while): ");
        int i=1;
        while(i<=10){
            System.out.print(i+" ");
            i++;
        }
        System.out.println("");
    }
}
