    private void delGoods() {
        if (txtID.getText().trim().equalsIgnoreCase("")) {
            JOptionPane.showMessageDialog(this, "Xin điền mã sản phẩm cần xóa");
            txtID.requestFocus();
            return;
        }
        String sql = "{call sp_delGoods(?)}";
        try (Connection conn = ConnectDatabase.getConnectDatabase();
                CallableStatement cstmt = conn.prepareCall(sql);) {
            cstmt.setString(1, txtID.getText());
            cstmt.executeUpdate();
        } catch (SQLException ex) {

            JOptionPane.showMessageDialog(this, "Lỗi xóa dữ liệu \n" + ex.getMessage());
        }
    }

    private void showToEditText() {
        int selectedRow = tblWareHouse.getSelectedRow();
        txtID.setText((String) tblWareHouse.getValueAt(selectedRow, 0));
        txtName.setText((String) tblWareHouse.getValueAt(selectedRow, 1));
    }

